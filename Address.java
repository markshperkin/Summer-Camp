public class Address {

    private String  street;
    private String town;
    private String state;
    private int zipCode;
    private String country;

    public Address (String street, String town, String state, int zipCode, String country ){
        this.street = street;
        this.town = town;
        this.state = state;
        this.zipCode = zipCode;
        this.country = country;
    }
    
}
